bool fget(uint8_t tile, uint8_t flag) {
    return (mask[tile] & (1 << flag)) != 0;
}

gfx_rletsprite_t *render_map(int cell_x, int cell_y, uint8_t layers) {
    gfx_SetColor(0);
    gfx_FillRectangle_NoClip(LCD_WIDTH / 2, 0, 128, 128);
    for(int y = 0; y < 16; y++) {
        for(int x = 0; x < 16; x++) {
            uint8_t tile = tilemap[x + cell_x + (y + cell_y) * 128];
            // I don't think this is how the PICO-8 actually handles the layers argument but whatevs
            if(tile && fget(tile, layers)) {
                gfx_TransparentSprite_NoClip(atlas_tiles[tile], LCD_WIDTH / 2 + x * 8, y * 8);
            }
        }
    }
    auto *sprite = (gfx_sprite_t*)&gfx_vbuffer[128][0];
    sprite->width = 128;
    sprite->height = 128;
    gfx_GetSprite(sprite, LCD_WIDTH / 2, 0);
    return gfx_ConvertMallocRLETSprite(sprite);
}

void map(int cell_x, int cell_y, int sx, int sy, uint8_t layers) {
    profiler_add(map);
    static struct {
        int x;
        int y;
        gfx_rletsprite_t *sprite;
    } cache[3] = {};
    auto entry = &cache[layers - 1];
    if(entry->x != cell_x || entry->y != cell_y || !entry->sprite) {
        entry->x = cell_x;
        entry->y = cell_y;
        free(entry->sprite);
        entry->sprite = render_map(cell_x, cell_y, layers);
    }
    if(entry->sprite) {
        gfx_RLETSprite(entry->sprite, SCREEN_X(sx), SCREEN_Y(sy));
    }
    profiler_end(map);
}

void spr(uint8_t n, int x, int y) {
    spr(n, x, y, 1, 1, false, false);
}

void spr(uint8_t n, int x, int y, uint8_t w, uint8_t h, bool flip_x, bool flip_y) {
    (void)w,(void)h;
    profiler_add(spr);
    gfx_sprite_t *sprite = atlas_tiles[n];
    gfx_TempSprite(temp, 8, 8);
    if(flip_x) {
        gfx_FlipSpriteY(sprite, temp);
    } else if(flip_y) {
        gfx_FlipSpriteX(sprite, temp);
    } else {
        memcpy(&temp_data, sprite, sizeof temp_data);
    }
    if(!default_pal) {
        for(uint8_t i = 0; i < 64; i++) {
            // this is probably slow
            temp->data[i] = pal_map[temp->data[i] & 0xF];
        }
    }
    // todo: both?
    gfx_TransparentSprite(temp, SCREEN_X(x), SCREEN_Y(y));
    profiler_end(spr);
}

void pal() {
    gfx_SetPalette(mypalette, sizeof mypalette, 0);
    for(uint8_t i = 0; i < 16; i++) {
        pal_map[i] = i | (i << 4);
    }
    default_pal = true;
}

void pal(int c0, int c1) {
    pal_map[c0] = c1 | (c1 << 4);
    default_pal = c0 == c1;
}

void pal(int c0, int c1, int p) {
    if(p == 0) {
        pal(c0, c1);
    } else {
        gfx_palette[c0] = mypalette[c1];
        default_pal = c0 == c1;
    }
}


bool btn(uint8_t index) {
    if(tas) {
        if(is_title() && index == 4) {
            return true;
        }
        return (tas_keys >> index) & 1;
    } else {
        kb_lkey_t keys[6] = {kb_KeyLeft, kb_KeyRight, kb_KeyUp, kb_KeyDown, kb_KeyPrgm, kb_KeySto};
        if(index > 5) return false;
        return kb_IsDown(keys[index]);
    }
}

